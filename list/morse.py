morse_code = ['.-','-...','-.-.','-..','.','..-.','--.','....','..','.---','-.-','.-..','--','-.','---','.--.','--.-','.-.','...','-','..-','...-','.--','-..-','-.--','--..','.----','..---','...--','....-','.....','-....','--...','---..','----.','-----','--..--','.-.-.-','..--..','-..-.','-....-','-.--.','-.--.-']

text = str(input('Enter any text: '))

morse_srt = ''

for x in text:
    morse_srt += morse_code[ord(x) - 97] + ' '

print('Morse Code for inserted text is: ',morse_srt)
